//! The `Ask`/`Answer` contract — the one-way door of the subagent layer
//! (ARCHITECTURE §18.1, ROADMAP T0.1).
//!
//! Wire stability: every struct/enum here is `#[non_exhaustive]`, serializes
//! with stable snake_case field names, and deserializes with `#[serde(default)]`
//! on every non-required field so older JSON keeps loading as the contract
//! grows. The committed JSON schemas (`schemas/ask.schema.json`,
//! `schemas/answer.schema.json`) are pinned by `tests/contract.rs`.

use serde::{Deserialize, Serialize};
use serde_json::Value;

/// Opaque identifier of one immutable stored trace (ARCHITECTURE §19.1).
///
/// Generated by the trace store; carried on [`Ask`] to resume/fork and
/// returned on every [`Answer`]. The wire form is a plain JSON string.
#[derive(Clone, Debug, PartialEq, Eq, Hash, PartialOrd, Ord, Serialize, Deserialize)]
#[serde(transparent)]
pub struct TraceId(String);

impl TraceId {
    /// Wrap an identifier produced by a trace store.
    pub fn new(id: impl Into<String>) -> Self {
        Self(id.into())
    }

    /// The identifier as a string slice.
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl std::fmt::Display for TraceId {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(&self.0)
    }
}

impl From<TraceId> for String {
    fn from(id: TraceId) -> String {
        id.0
    }
}

/// Permission bits applied when a blob is materialized into the scratchpad
/// (ARCHITECTURE §18.3). Enforcement v1 is materialize-with-mode-bits inside
/// the jail; anything stronger is a host upgrade behind the same type.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[non_exhaustive]
pub struct BlobPerms {
    pub read: bool,
    #[serde(default)]
    pub write: bool,
    #[serde(default)]
    pub execute: bool,
}

impl BlobPerms {
    /// Explicit permission bits.
    pub fn new(read: bool, write: bool, execute: bool) -> Self {
        Self {
            read,
            write,
            execute,
        }
    }

    /// The default for inbound material: readable, nothing else.
    pub fn read_only() -> Self {
        Self::new(true, false, false)
    }
}

impl Default for BlobPerms {
    fn default() -> Self {
        Self::read_only()
    }
}

/// How a blob's bytes are referenced (ARCHITECTURE §18.3): inline, by path on
/// the local filesystem, or content-addressed into the blob store.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "kind", rename_all = "snake_case")]
#[non_exhaustive]
pub enum BlobRef {
    /// Inline bytes, base64-encoded on the wire.
    Bytes { base64: String },
    /// A path readable by the receiving host (orchestrator-local hand-in).
    Path { path: String },
    /// A content-addressed ref into the `hugr-replay` blob store (sha-256 hex).
    Sha256 { sha256: String },
}

/// A file handed into or out of an ask — a typed contract slot, not `extra`,
/// because orchestrator↔agent file flow is part of the contract (§18.3).
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[non_exhaustive]
pub struct BlobHandle {
    /// Where the bytes are.
    #[serde(rename = "ref")]
    pub blob_ref: BlobRef,
    /// IANA media type of the payload (e.g. `application/pdf`).
    pub media_type: String,
    /// Mode bits applied on materialization.
    #[serde(default)]
    pub perms: BlobPerms,
    /// Suggested file name inside the scratchpad; the host may sanitize it.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub name: Option<String>,
}

impl BlobHandle {
    /// A handle with default (read-only) perms and no name hint.
    pub fn new(blob_ref: BlobRef, media_type: impl Into<String>) -> Self {
        Self {
            blob_ref,
            media_type: media_type.into(),
            perms: BlobPerms::default(),
            name: None,
        }
    }

    /// Override the materialization permission bits.
    pub fn with_perms(mut self, perms: BlobPerms) -> Self {
        self.perms = perms;
        self
    }

    /// Suggest a file name for materialization inside the scratchpad.
    pub fn with_name(mut self, name: impl Into<String>) -> Self {
        self.name = Some(name.into());
        self
    }
}

/// One question to a subagent (ARCHITECTURE §18.1). `question` is the only
/// required field; everything else defaults.
#[derive(Clone, Debug, PartialEq, Serialize, Deserialize)]
#[non_exhaustive]
pub struct Ask {
    /// The question text.
    pub question: String,
    /// Resume/fork anchor: run this ask as a follow-up of the identified
    /// stored trace (§19.2). `None` starts a fresh root trace.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub trace_id: Option<TraceId>,
    /// Inbound files, materialized into the scratchpad before the turn (§18.3).
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub blobs: Vec<BlobHandle>,
    /// Opaque caller metadata, echoed into the trace. Never interpreted.
    #[serde(default, skip_serializing_if = "Value::is_null")]
    pub extra: Value,
}

impl Ask {
    /// An ask with only the required question; everything else defaulted.
    pub fn new(question: impl Into<String>) -> Self {
        Self {
            question: question.into(),
            trace_id: None,
            blobs: Vec::new(),
            extra: Value::Null,
        }
    }

    /// Anchor this ask on a stored trace (resume; ask twice from the same
    /// parent to fork).
    pub fn with_trace_id(mut self, trace_id: TraceId) -> Self {
        self.trace_id = Some(trace_id);
        self
    }

    /// Hand files in.
    pub fn with_blobs(mut self, blobs: Vec<BlobHandle>) -> Self {
        self.blobs = blobs;
        self
    }

    /// Attach opaque caller metadata.
    pub fn with_extra(mut self, extra: Value) -> Self {
        self.extra = extra;
        self
    }
}

/// The tri-state outcome every surface branches on. Errors are answers —
/// `Error` still returns a full [`Answer`] (and exit code 0 on a CLI).
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
#[non_exhaustive]
pub enum AnswerStatus {
    /// The agent answered the question.
    Success,
    /// The question is outside this agent's declared domain.
    OffTopic,
    /// The run failed (limits, transport, no final answer, …); `message`
    /// carries the human-readable reason.
    Error,
}

/// Per-tier spend line inside [`AnswerMeta`] (ARCHITECTURE §18.4).
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[non_exhaustive]
pub struct TierSpend {
    /// The tier selector this line aggregates (`small` / `medium` / `big`).
    pub selector: String,
    /// Model calls made on this tier.
    pub model_calls: u32,
    /// Prompt tokens consumed on this tier.
    pub tokens_in: u64,
    /// Completion tokens produced on this tier.
    pub tokens_out: u64,
    /// Cost of this tier's usage, in micro-USD.
    pub cost_micro_usd: u64,
}

impl TierSpend {
    /// One aggregated spend line for a tier.
    pub fn new(
        selector: impl Into<String>,
        model_calls: u32,
        tokens_in: u64,
        tokens_out: u64,
        cost_micro_usd: u64,
    ) -> Self {
        Self {
            selector: selector.into(),
            model_calls,
            tokens_in,
            tokens_out,
            cost_micro_usd,
        }
    }
}

/// Mandatory accounting on every [`Answer`] (ARCHITECTURE §18.4). Derivable
/// from the persisted trace alone — `OpMeta` usage × per-tier pricing —
/// including sub-agent children.
#[derive(Clone, Debug, Default, PartialEq, Eq, Serialize, Deserialize)]
#[non_exhaustive]
pub struct AnswerMeta {
    /// Wall-clock duration of the ask, in milliseconds.
    pub duration_ms: u64,
    /// Total cost in micro-USD (1 USD = 1_000_000).
    pub cost_micro_usd: u64,
    /// Total prompt tokens across all model calls (children included).
    pub tokens_in: u64,
    /// Total completion tokens across all model calls (children included).
    pub tokens_out: u64,
    /// Number of model calls made.
    pub model_calls: u32,
    /// Number of tool invocations made.
    pub tool_calls: u32,
    /// Per-tier breakdown; sums to the totals above.
    #[serde(default)]
    pub per_tier: Vec<TierSpend>,
}

impl AnswerMeta {
    /// Zeroed accounting (an error before any model call still carries meta).
    pub fn new() -> Self {
        Self::default()
    }

    /// Set the wall-clock duration.
    pub fn with_duration_ms(mut self, duration_ms: u64) -> Self {
        self.duration_ms = duration_ms;
        self
    }

    /// Set the tool-call count.
    pub fn with_tool_calls(mut self, tool_calls: u32) -> Self {
        self.tool_calls = tool_calls;
        self
    }

    /// Append one tier's spend line and fold it into the totals.
    pub fn with_tier(mut self, tier: TierSpend) -> Self {
        self.cost_micro_usd += tier.cost_micro_usd;
        self.tokens_in += tier.tokens_in;
        self.tokens_out += tier.tokens_out;
        self.model_calls += tier.model_calls;
        self.per_tier.push(tier);
        self
    }
}

/// The uniform result of one ask (ARCHITECTURE §18.1).
#[derive(Clone, Debug, PartialEq, Serialize, Deserialize)]
#[non_exhaustive]
pub struct Answer {
    /// Outcome to branch on.
    pub status: AnswerStatus,
    /// The answer text (or the error text when `status` is `Error`).
    pub message: String,
    /// The **new** trace this run persisted (§19.2) — the anchor for
    /// follow-ups and forks.
    pub trace_id: TraceId,
    /// Outbound files, content-addressed (§18.3).
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub blobs: Vec<BlobHandle>,
    /// Mandatory accounting.
    pub metadata: AnswerMeta,
    /// Agent-specific structured extras (schema-declarable, ROADMAP T3.4);
    /// never load-bearing for the contract.
    #[serde(default, skip_serializing_if = "Value::is_null")]
    pub extra: Value,
}

impl Answer {
    /// An answer with the required fields; blobs/extra default empty.
    pub fn new(
        status: AnswerStatus,
        message: impl Into<String>,
        trace_id: TraceId,
        metadata: AnswerMeta,
    ) -> Self {
        Self {
            status,
            message: message.into(),
            trace_id,
            blobs: Vec::new(),
            metadata,
            extra: Value::Null,
        }
    }

    /// Attach outbound files.
    pub fn with_blobs(mut self, blobs: Vec<BlobHandle>) -> Self {
        self.blobs = blobs;
        self
    }

    /// Attach agent-specific structured extras.
    pub fn with_extra(mut self, extra: Value) -> Self {
        self.extra = extra;
        self
    }
}
